"""Helper functions for persona-aware rendering and icon management."""

from flask import g


def persona_can_see(feature_id, persona=None):
    """Check if current persona can see a feature."""
    if persona is None:
        persona = g.get('persona', 'committee')

    # Staff can see everything
    if persona == 'staff':
        return True

    # Define what committee can see
    committee_features = {
        'overview', 'projects', 'schools', 'ask_ai',
        'concerns', 'watchlist', 'risk',
        'financials', 'documents', 'meeting_minutes', 'annual_report',
        'meeting_mode', 'contracts_dashboard', 'contracts_list'
    }
    return feature_id in committee_features


def get_overview_template_for_persona(persona='committee', view_mode='surtax'):
    """Return appropriate overview template based on persona and view mode."""
    if view_mode == 'contracts':
        if persona == 'committee':
            return 'contracts/dashboard.html'
        return 'contracts/executive_dashboard.html'
    else:
        if persona == 'committee':
            return 'surtax/committee_overview.html'
        return 'surtax/executive_dashboard.html'


def should_hide_sidebar(endpoint, persona='committee'):
    """Determine if sidebar should be hidden for a given route and persona."""
    if persona == 'committee' and endpoint in ['tools.meeting_mode', 'tools.meeting_present']:
        return True
    return False


